static uint8_t load_epaperfile()
{
	FIL fp; 
	uint8_t file[] = "2:epaper/epaper.bin";
	uint8_t head[6];
	uint32_t len;
	if(false == IntFlash_Open(&fp,file))
	{
		TRACELOG_DEBUG("Open epaper file fail...");
		return false;
	}

	f_read(&fp, head, 6, &len);
	if(len != 6)
	{
		TRACELOG_DEBUG("Load epaper file head fail...");
		IntFlash_Close(&fp);
		return false;
	}
	
	f_read(&fp, (uint8_t*)Hinata.bmp.body, EPD_FRAMEBUF_SIZE_2SCALE, &len);
	if(len != EPD_FRAMEBUF_SIZE_2SCALE)
	{
		TRACELOG_DEBUG("Load epaper file body fail...");
		IntFlash_Close(&fp);
		return false;
	}
	IntFlash_Close(&fp);
	//TRACELOG_DEBUG("Load epaper file body success...");
	return true;
}

