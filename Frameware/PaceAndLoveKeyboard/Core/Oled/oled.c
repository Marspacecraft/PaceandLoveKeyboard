typedef struct
{
	uint8_t 	title_enable;
	uint8_t 	title_flowfg;//列字符溢出标志	
	
	uint16_t 	title_buflen;
	uint16_t 	title_showpos;//列显示开始位置
	uint8_t 	title_showposdelay;//列滚动开始延时
	
	
}oledtile;


typedef struct
{
	oledtile titles[OLED_BUFFER_HEIGHT];
	
	uint8_t line_showorder[OLED_BUFFER_HEIGHT];//滚动显示列顺序队列
	uint8_t linepos;//当前起始显示队列
	uint8_t linenum;//显示列个数
	
}oledbufmsg_t;

static oledbufmsg_t sg_oledbuffermsg = {0};
static uint8_t sg_oledbuffer[2*OLED_BUFFER_HEIGHT][OLED_BUFFER_WIDTH] ={0};


static void Oled_Title2ShowOrder(uint8_t title)
{
	uint8_t i=0;
	for(i=0;i<sg_oledbuffermsg.linenum;i++)
	{
		if(sg_oledbuffermsg.line_showorder[i] == title)
			return;

	}

	if(i<OLED_BUFFER_HEIGHT)
	{
		sg_oledbuffermsg.line_showorder[i] = title;
		sg_oledbuffermsg.linenum++;
	}
}

void OLED_CharMode_SetShowOrder(uint8_t* titles)
{
	uint8_t i;
	for(i=0;i<OLED_BUFFER_HEIGHT;i++)
	{
		if(OLED_TITLE_NULL <= titles[i])
		{	
			break;
		}
		sg_oledbuffermsg.line_showorder[i] = titles[i];
	}

	sg_oledbuffermsg.linenum = i;
	sg_oledbuffermsg.linepos = 0;
}

void OLED_CharMode_ClearShowOrder()
{
	sg_oledbuffermsg.linenum = 0;
	sg_oledbuffermsg.linepos = 0;

}


static void Oled_ShowTitle2Line(uint8_t title,uint8_t line)
{
	uint16_t i = 0;
	uint16_t title_buflen;
	uint16_t title_showpos;//列显示开始位置
	uint8_t j;

	if(title>=OLED_BUFFER_HEIGHT || line>=2)
		return;

	line = line << 1;
	if(0 == sg_oledbuffermsg.titles[title].title_enable)
		return;

	title_buflen = sg_oledbuffermsg.titles[title].title_buflen;
	title_showpos = sg_oledbuffermsg.titles[title].title_showpos;
	title = title << 1;
	for(j=0;j<2;j++,line++)
	{
		OLED_Set_Position(0, line);
		
		OLED_Write_databuffer(&sg_oledbuffer[title+j][0], 8);
		
		i = title_buflen - title_showpos;
		if(i>120)
		{
			i = 120;
		}
		OLED_Write_databuffer(&sg_oledbuffer[title+j][title_showpos], i);
		
		i+=8;
		for (; i <OLED_WIDTH; i++)
		{
			OLED_Write_data(0x00);
		}
	}
}


static uint8_t Oled_Append2Title(uint8_t title,uint8_t* font,uint16_t len)
{
	oledtile* titlemsg;
	uint8_t ret = true;
	if(title>=OLED_BUFFER_HEIGHT)
		return false;

	titlemsg = &sg_oledbuffermsg.titles[title];

	if((len + titlemsg->title_buflen)>OLED_BUFFER_WIDTH)
	{
		len = OLED_BUFFER_WIDTH - titlemsg->title_buflen;
		ret = false;
	}

	if(titlemsg->title_buflen>OLED_WIDTH)
	{
		titlemsg->title_flowfg = 1;
	}

	title = title << 1;
	memcpy(sg_oledbuffer[title]+titlemsg->title_buflen,font,len);
	memcpy(sg_oledbuffer[title+1]+titlemsg->title_buflen,font+len,len);
	titlemsg->title_buflen += len;
	return ret;
	
}

static void Oled_Reset2Title(uint8_t title,uint8_t* font,uint16_t len)
{
	oledtile* titlemsg;
	if(title>=OLED_BUFFER_HEIGHT)
		return;
	
	titlemsg = &sg_oledbuffermsg.titles[title];
	if(NULL == font)
	{
		titlemsg->title_enable = 0;
		return;
	}
	titlemsg->title_enable = 1;
	titlemsg->title_buflen = 8;
	titlemsg->title_showpos = 8;
	titlemsg->title_showposdelay = 15;
	

	
	memcpy(sg_oledbuffer[2*title],&TITLEF[16*title],8);
	memcpy(sg_oledbuffer[2*title+1],&TITLEF[16*title+8],8);

	Oled_Append2Title(title,font,len);

}

static void Oled_Flush_Line(uint8_t lineflushfg)
{
	uint8_t line,title,i,num;
	oledtile* titlemsg;
	num = (2<sg_oledbuffermsg.linenum)?2:sg_oledbuffermsg.linenum;

	line=sg_oledbuffermsg.linepos;
	
	for(i=0;i<num;)
	{	
		title = sg_oledbuffermsg.line_showorder[line];
		titlemsg = &sg_oledbuffermsg.titles[title];
		if(titlemsg->title_enable)
		{
			if(lineflushfg)//刷新所有行
			{
				Oled_ShowTitle2Line(title,i);
			}
			else
			{//只刷新溢出行
				if(titlemsg->title_flowfg)
				{
					if(0==titlemsg->title_showposdelay)
					{
						titlemsg->title_showpos += 2;
						if((titlemsg->title_buflen - titlemsg->title_showpos)<=80)
						{
							titlemsg->title_showpos = 8;
							titlemsg->title_showposdelay = 15;
						}
						Oled_ShowTitle2Line(title,i);
					}
					else
					{
						titlemsg->title_showposdelay--;
					}
				}
			}
			i++;
		}
		
		line++;
		line %= sg_oledbuffermsg.linenum;
		if(line == sg_oledbuffermsg.linepos)
		{
			break;
		}
	}
}

void OLED_CharMode_ShowString(uint8_t title,uint8_t* string)
{
	uint16_t i,len;
	if(title>=OLED_BUFFER_HEIGHT || string[0] < ' ')
		return;

	Oled_Reset2Title(title,&F8X16[(string[0]-' ')*16],8);
	len = strlen((char*)string);
	for(i=1;i<len;i++)
	{
		if(false == Oled_Append2Title(title,&F8X16[(string[i]-' ')*16],8))
		{
			break;
		}
	}
	Oled_Title2ShowOrder(title);
	Oled_Flush_Line(true);
}

void OLED_CharMode_ShowStringAppend(uint8_t title,uint8_t* string)
{
	uint16_t i,len;
	if(title>=OLED_BUFFER_HEIGHT)
		return;
	len = strlen((char*)string);
	for(i=0;i<len;i++)
	{
		if(false == Oled_Append2Title(title,&F8X16[(string[i]-' ')*16],8))
		{
			break;
		}
	}
	
	Oled_Flush_Line(true);
}



void OLED_CharMode_Flush(uint8_t* arg)
{
	static uint8_t linetime = 30;

	if((sg_oledbuffermsg.linenum > 2)&&(0 == linetime))
	{
		sg_oledbuffermsg.linepos += 2;
		if(sg_oledbuffermsg.linepos >= sg_oledbuffermsg.linenum)
			sg_oledbuffermsg.linepos = 0;
		linetime = 30;
		Oled_Flush_Line(true);
		return;
	}
	linetime--;

	Oled_Flush_Line(false);

}




static CycleTimerHandler* sg_oledtimer = NULL;

void OLED_CharMode_DeInit()
{
	if(sg_oledtimer)
	{
		Stop_CycleTimer(sg_oledtimer);
		sg_oledtimer = NULL;
	}
	OLED_Display_Off();
}

void OLED_CharMode_Init()
{
	memset(sg_oledbuffer,0,sizeof(sg_oledbuffer));
	memset(&sg_oledbuffermsg,0,sizeof(sg_oledbuffermsg));
	OLED_Clear();
	OLED_Set_Position(0, 0);
	
	if(NULL == sg_oledtimer)
	{
		sg_oledtimer = Start_CycleTimer(TIMER_PERIOD_100MS,OLED_CharMode_Flush,NULL);
	}
	OLED_Display_On();
}

